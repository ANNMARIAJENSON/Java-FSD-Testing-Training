package com.java.assignment;

public class ArithmeticOperations {

	public static void main(String[] args) {
		int a=20,b=10;
		System.out.println("Arithmetic Operations");
		int sum=a+b;
		int sub=a-b;
		int mul=a*b;
		int div=a/b;
		int mod=a%b;
		System.out.println("Addition of two variables is : "+sum);
		System.out.println("Subtraction of two variables is : "+sub);
		System.out.println("Multiplication of two variables is : "+mul);
		System.out.println("Division of two variables is : "+div);
		System.out.println("Modulus of two variables is : "+mod);
	}

}
